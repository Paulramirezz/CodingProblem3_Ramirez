let age = "20";

if (age < 5) {
    console.log("You are a toddler.");
} else if (age <= 9) {
    console.log("You are a child.");
} else if (age <= 11) {
    console.log("You are a teenager.");
} else if (age <= 20) {
    console.log("You are a young adult.");
} else if (age <= 60) {
    console.log("You are middle-aged.");
} else {
    console.log("You are a senior.")
}
